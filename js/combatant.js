	updatePassive(){
		if(this.type<=0){
			this.life=0
			this.fade=0
		}
		if(this.life<=0&&this.fade>0&&!this.dieTrigger){
			this.fade-=1/30
		}
		if(this.life>0&&this.fade<1){
			this.fade+=1/30
		}
		if(this.life<=0&&this.collect.life<=0){
			this.life=-10000
			this.collect.life=-10000
		}
		this.collect.life=this.collect.life*0.9+this.life*0.1
	}
